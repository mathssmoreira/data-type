public class TiposNumericosTodas {

    //Tipos Numéricos de variávels e suas capacidades

    public static void main(String[] args) {

        //A diferença entre variaveis normais e do tipo primitivo é que as normais podemos declarar como null (Ausente de valor)

        byte variavelByte = 127;

        short variavelShort = -32768;

        int variavelInt = 2147483647;

        long variavelLong = 1L;

        float variavelFloat = 1.0F;

        double variavelDouble = 1.0;

        char variavelChar = 'A';

        boolean variavelBoolean = true;
    }

}