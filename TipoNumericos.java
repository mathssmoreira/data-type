public class TiposNumericos {

	//Tipos Numéricos de variávels e suas capacidades
	
	public static void main(String[] args) {

	Byte numerByte = 127; 		     // -128 até 127
	
	Short numeroShort = -32768;    	     // -32768 até 32767
	
	Integer numeroInteger = 2147483647;  // -2147483648 até 2147483647
	
	Long numeroLong = 1L;                // -9,223,372,037x10^18 até 9,223,372,037x10^18
	
	Float numeroLong = 1.0F; 	     // mesma capacidade que o Integer porém decimal
	
	Double numeroDouble = 1.0;           // mesma capacidade que o Long porém decimal
		
	}

}